package org.zerock.ThisIsJavaExample;

public class Ch02_01 {
	
	public static void main(String[] args) {
		
		// 변수 value에 10을 초기값으로 저장
		int value = 10;
		
		// 변수에 10을 더하는 산술 연산을 수행하고 이를 변수 result에 저장
		int result = value + 10; // = 20
		
		// 콘솔창에 출력
		System.out.println(result);
		
	} // main

} // end class
